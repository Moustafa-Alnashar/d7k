def main():
    ser = serial.Serial(SERIAL_PORT, BAUD_RATE)
    wf = wave.open(TEMP_WAV, 'wb')
    wf.setnchannels(1)
    wf.setsampwidth(1) 
    wf.setframerate(SAMPLE_RATE)

    print("🎙️ Streaming and Recording... Listening for words...")
    print("Press Ctrl+C to stop and convert to MP3.")

    audio_buffer = []
    samples_needed_for_1_sec = SAMPLE_RATE 

    try:
        while True:
            raw_data = ser.read(CHUNK_SIZE)
            if not raw_data:
                continue

            # Convert to int8 (-128 to 127) as per original script
            temp = np.frombuffer(raw_data, dtype=np.uint8).astype(np.int16)
            # print(f"{temp.max()} {temp.min()} {temp.mean():.2f}")
            centered_data = (temp - 128).astype(np.int8)
            
            # Write to WAV file to keep recording functionality
            wf.writeframes(centered_data.tobytes())

            # Append to real-time rolling buffer
            # audio_buffer.extend(centered_data)
            audio_buffer.extend(temp)

            # Every 1 second of audio data collected, attempt classification
            if len(audio_buffer) >= samples_needed_for_1_sec:
                y_data = np.array(audio_buffer[:samples_needed_for_1_sec])
                
                # Check for silence threshold so it doesn't classify background noise endlessly
                if np.max(np.abs(y_data)) > 15: # Arbitrary threshold, adjust if it ignores quiet speech
                    
                    # Extract
                    test_zcr, test_ste = extract_features_realtime(y_data, current_sr=SAMPLE_RATE, target_sr=TARGET_SR)
                    
                    # Classify (Manhattan Distance against templates)
                    min_dist = float('inf')
                    predicted_label = -1
                    
                    for j in range(len(WORDS)):
                        dist_zcr = np.sum(np.abs(test_zcr - zcr_templates[j]))
                        dist_ste = np.sum(np.abs(test_ste - ste_templates[j]))
                        
                        total_dist = (W_ZCR * dist_zcr) + (W_STE * dist_ste)
                        
                        if total_dist < min_dist:
                            min_dist = total_dist
                            predicted_label = j

                    print(f"🎯 Detected Word: {WORDS[predicted_label]} (Score: {min_dist:.2f})")
                
                # Clear buffer (or slide window)
                # To clear entirely for isolated words:
                audio_buffer = []

    except KeyboardInterrupt:
        print("\n🛑 Stopped. Processing audio for WhatsApp...")

    finally:
        wf.close()
        ser.close()

        # --- CONVERSION STEP ---
        if os.path.exists(TEMP_WAV):
            try:
                audio = AudioSegment.from_wav(TEMP_WAV)
                audio.export(FINAL_MP3, format="mp3", bitrate="128k")
                print(f"✅ Success! '{FINAL_MP3}' is ready to be sent on WhatsApp.")
            except Exception as e:
                print(f"❌ Error during conversion: {e}")
                print("Make sure FFmpeg is installed and in your PATH.")

if __name__ == "__main__":
    main()