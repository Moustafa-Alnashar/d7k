# --- Real-Time Feature Extraction ---
def extract_features_realtime(y, current_sr, target_sr=8000):
    # Librosa expects float32 from -1.0 to 1.0
    y_float = y.astype(np.float32) / 128.0

    # y_float = y_float - y_float.mean()  # Center around zero
    
    # Resample down to 8000Hz like in the training phase
    if current_sr != target_sr:
        y_float = librosa.resample(y_float, orig_sr=current_sr, target_sr=target_sr)

    # Trim silence (matches notebook logic)
    y_trimmed, _ = librosa.effects.trim(y_float, top_db=15)
    
    # We need exactly 1 second (8000 samples)
    target_len = target_sr * 1 
    if len(y_trimmed) < target_len:
        pad_total = target_len - len(y_trimmed)
        y_fixed = np.pad(y_trimmed, (pad_total // 2, pad_total - (pad_total // 2)))
    else:
        cut_total = len(y_trimmed) - target_len
        y_fixed = y_trimmed[cut_total // 2 : len(y_trimmed) - (cut_total - cut_total // 2)]

    # Pre-emphasis
    y_fixed = librosa.effects.preemphasis(y_fixed, coef=0.95)

    # Extract ZCR & STE
    frames = librosa.util.frame(y_fixed, frame_length=160, hop_length=80)
    zcr = librosa.feature.zero_crossing_rate(y_fixed, frame_length=160, hop_length=80, center=False)[0]
    ste = np.mean(frames**2, axis=0)

    # Force to N_FEATURES (99) to match your C templates
    min_len = min(len(zcr), len(ste), N_FEATURES)
    zcr = zcr[:min_len]
    ste = ste[:min_len]

    # Smoothing
    smooth_window = 5
    zcr = np.convolve(zcr, np.ones(smooth_window)/smooth_window, mode='same')
    ste = np.convolve(ste, np.ones(smooth_window)/smooth_window, mode='same')

    if np.max(ste) > 0:
        ste = ste / np.max(ste)

    # Pad to exactly 99 if slightly short
    if len(zcr) < N_FEATURES:
        zcr = np.pad(zcr, (0, N_FEATURES - len(zcr)))
        ste = np.pad(ste, (0, N_FEATURES - len(ste)))
    else:
        zcr = zcr[:N_FEATURES]
        ste = ste[:N_FEATURES]

    
    print(f"{zcr.max()} {zcr.min()} {zcr.mean():.2f}")
    print(f"{ste.max()} {ste.min()} {ste.mean():.2f}")

    return zcr, ste

